package sudoku.problemdomain;

import java.util.Objects;

/*
Represents x and y coordinates for the user interface. It will be used as keys to keep track of different UI elements.
 */

public class Coordinates {
    private final int x;
    private final int y;

    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (this == null || getClass() != o.getClass()) return false;
        Coordinates that = (Coordinates) o;
        return x == that.x &&
                y == that.y;
    }

    @Override
    public int hashCode(){
        return Objects.hash(x,y);
    }
}
